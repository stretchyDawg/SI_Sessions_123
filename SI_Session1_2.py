def printtest():
    """
    This function showcases the sep and end commands
    """
    # Sep Command
    print("\n", "Christian", "Jackson")
    # This puts a space between the two strings because the separater always separates the concatenation with a space
    print("Abe", "Christian", "Irwin", sep = "Jackson", end = "")


    print("\n")

    # End Command
    print("Abe")
    print("Christian")
    # This creates a new line because \n is at the end of every command
    print("Abe", end = "poop")
    print("Christian")

